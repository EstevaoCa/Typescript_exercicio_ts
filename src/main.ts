// Função de multiplicação
function multiplicar(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudacao(nome: string): string {
    return `Olá ${nome}`;
}

// Exemplos de uso
console.log(multiplicar(2, 3)); // Saída: 6
console.log(saudacao("Estevão")); // Saída: Olá Estevão
